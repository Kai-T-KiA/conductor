# ==============================================================================
# クライアントモデル
# ==============================================================================
# 目的: クライアント（企業）情報の管理
# 特徴:
# - プロジェクト、タスク、月次支払いとの関連付け
# ==============================================================================
class Client < ApplicationRecord
  # リレーションシップ定義
  # クライアントは複数のプロジェクト、月次支払いを持つ
  # dependent: :destroy により、クライアントが削除されると関連するプロジェクトも削除される
  has_many :projects, dependent: :destroy
  has_many :monthly_payments
  has_many :tasks, through: :projects  # プロジェクトを通じてタスクにアクセス可能

  # バリデーション設定
  validates :name, presence: true  # クライアント名は必須
end